def tw_to_cn(text: str) -> str:
    """Convert Traditional Chinese to Simplified Chinese."""
    # Word-level replacements first (longer phrases before shorter)
    result = text
    for tw, cn in TW_TO_CN_WORDS:
        result = result.replace(tw, cn)
    # Character-level conversion
    result = result.translate(TW_TO_CN_CHARS)
    return result


def fill_template(template_path: Path, translations_path: Path):
    """Fill a template JSON with translations."""
    template = json.loads(template_path.read_text(encoding="utf-8"))
    translations = json.loads(translations_path.read_text(encoding="utf-8"))

    filled = 0
    for idx_str, zh_tw in translations.items():
        if idx_str in template["entries"]:
            template["entries"][idx_str]["zh_tw"] = zh_tw
            template["entries"][idx_str]["zh_cn"] = tw_to_cn(zh_tw)
            filled += 1

    template_path.write_text(
        json.dumps(template, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"  Filled {filled} entries in {template_path.name}")


def fill_template_from_dict(template_path: Path, translations: dict):
    """Fill a template JSON with translations dict directly."""
    template = json.loads(template_path.read_text(encoding="utf-8"))

    filled = 0
    for idx_str, zh_tw in translations.items():
        if str(idx_str) in template["entries"]:
            template["entries"][str(idx_str)]["zh_tw"] = zh_tw
            template["entries"][str(idx_str)]["zh_cn"] = tw_to_cn(zh_tw)
            filled += 1

    template_path.write_text(
        json.dumps(template, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    return filled


def main():
    if len(sys.argv) < 3:
        print("Usage: python3 fill_translations.py <template.json> <translations.json>")
        print("   or: python3 fill_translations.py --batch <dir>  (process all .trans.json files)")
        sys.exit(1)

    if sys.argv[1] == "--batch":
        trans_dir = Path(sys.argv[2])
        for trans_file in sorted(trans_dir.rglob("*.trans.json")):
            template_file = trans_file.with_suffix("").with_suffix(".json")
            if template_file.exists():
                fill_template(template_file, trans_file)
    else:
        template_path = Path(sys.argv[1])
        translations_path = Path(sys.argv[2])
        fill_template(template_path, translations_path)


if __name__ == "__main__":
    main()
